public class Pessoa {
	
  private String nome;
  private boolean habilitado;
  
  public String getNome() {
    return nome;
  }
  
  public void setNome(String nome) {
    this.nome = nome;
  }
  
  public boolean isHabilitado() {
    return habilitado;
  }
  
  public void setHabilitado(boolean habilitado) {
    this.habilitado = habilitado;
  }
  
}